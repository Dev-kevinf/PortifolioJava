package Portifilio;

public class TabuadaAutomatica {
	public static void main(String[] args) {
		
		System.out.println("-----------Tabuada Automatica-----------");
		for (int i = 0; i <= 20; i++) {
			System.out.println("Tabuada do " + i + ":\n");
			for(int j=0; j<=20; j++) {
				System.out.printf(i + "X" + j +"= " +  (i * j + "\n"));
			}
			System.out.println("\n");
}

}

}